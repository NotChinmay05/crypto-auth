RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]


def _xtime(value: int) -> int:
    return ((value << 1) ^ 0x1B) & 0xFF if value & 0x80 else (value << 1)


def _mul(a: int, b: int) -> int:
    result = 0
    for _ in range(8):
        if b & 1:
            result ^= a
        a = _xtime(a)
        b >>= 1
    return result & 0xFF


def _bytes_to_state(block: bytes) -> list[list[int]]:
    return [[block[row + 4 * col] for col in range(4)] for row in range(4)]


def _state_to_bytes(state: list[list[int]]) -> bytes:
    return bytes(state[row][col] for col in range(4) for row in range(4))


def _xor(left: bytes, right: bytes) -> bytes:
    return bytes(a ^ b for a, b in zip(left, right))


def _rot_word(word: list[int]) -> list[int]:
    return word[1:] + word[:1]


def _sub_word(word: list[int]) -> list[int]:
    return [S_BOX[byte] for byte in word]


def _expand_key(key: bytes) -> list[list[list[int]]]:
    if len(key) != 16:
        raise ValueError("AES-128 key must be 16 bytes")
    words = [list(key[i : i + 4]) for i in range(0, 16, 4)]
    for i in range(NK, NB * (NR + 1)):
        temp = words[i - 1][:]
        if i % NK == 0:
            temp = _sub_word(_rot_word(temp))
            temp[0] ^= RCON[i // NK]
        words.append([words[i - NK][j] ^ temp[j] for j in range(4)])

    round_keys = []
    for round_index in range(NR + 1):
        state = [[0] * 4 for _ in range(4)]
        for col in range(4):
            word = words[round_index * 4 + col]
            for row in range(4):
                state[row][col] = word[row]
        round_keys.append(state)
    return round_keys


def _add_round_key(state: list[list[int]], round_key: list[list[int]]) -> None:
    for row in range(4):
        for col in range(4):
            state[row][col] ^= round_key[row][col]


def _sub_bytes(state: list[list[int]]) -> None:
    for row in range(4):
        for col in range(4):
            state[row][col] = S_BOX[state[row][col]]


def _inv_sub_bytes(state: list[list[int]]) -> None:
    for row in range(4):
        for col in range(4):
            state[row][col] = INV_S_BOX[state[row][col]]


def _shift_rows(state: list[list[int]]) -> None:
    for row in range(1, 4):
        state[row] = state[row][row:] + state[row][:row]


def _inv_shift_rows(state: list[list[int]]) -> None:
    for row in range(1, 4):
        state[row] = state[row][-row:] + state[row][:-row]


def _mix_columns(state: list[list[int]]) -> None:
    for col in range(4):
        a = [state[row][col] for row in range(4)]
        state[0][col] = _mul(a[0], 2) ^ _mul(a[1], 3) ^ a[2] ^ a[3]
        state[1][col] = a[0] ^ _mul(a[1], 2) ^ _mul(a[2], 3) ^ a[3]
        state[2][col] = a[0] ^ a[1] ^ _mul(a[2], 2) ^ _mul(a[3], 3)
        state[3][col] = _mul(a[0], 3) ^ a[1] ^ a[2] ^ _mul(a[3], 2)


def _inv_mix_columns(state: list[list[int]]) -> None:
    for col in range(4):
        a = [state[row][col] for row in range(4)]
        state[0][col] = _mul(a[0], 14) ^ _mul(a[1], 11) ^ _mul(a[2], 13) ^ _mul(a[3], 9)
        state[1][col] = _mul(a[0], 9) ^ _mul(a[1], 14) ^ _mul(a[2], 11) ^ _mul(a[3], 13)
        state[2][col] = _mul(a[0], 13) ^ _mul(a[1], 9) ^ _mul(a[2], 14) ^ _mul(a[3], 11)
        state[3][col] = _mul(a[0], 11) ^ _mul(a[1], 13) ^ _mul(a[2], 9) ^ _mul(a[3], 14)


def aes128_encrypt_block(block: bytes, key: bytes) -> bytes:
    if len(block) != BLOCK_SIZE:
        raise ValueError("AES block must be 16 bytes")
    round_keys = _expand_key(key)
    state = _bytes_to_state(block)
    _add_round_key(state, round_keys[0])
    for round_index in range(1, NR):
        _sub_bytes(state)
        _shift_rows(state)
        _mix_columns(state)
        _add_round_key(state, round_keys[round_index])
    _sub_bytes(state)
    _shift_rows(state)
    _add_round_key(state, round_keys[NR])
    return _state_to_bytes(state)


def aes128_decrypt_block(block: bytes, key: bytes) -> bytes:
    if len(block) != BLOCK_SIZE:
        raise ValueError("AES block must be 16 bytes")
    round_keys = _expand_key(key)
    state = _bytes_to_state(block)
    _add_round_key(state, round_keys[NR])
    for round_index in range(NR - 1, 0, -1):
        _inv_shift_rows(state)
        _inv_sub_bytes(state)
        _add_round_key(state, round_keys[round_index])
        _inv_mix_columns(state)
    _inv_shift_rows(state)
    _inv_sub_bytes(state)
    _add_round_key(state, round_keys[0])
    return _state_to_bytes(state)


def aes128_cbc_encrypt(plaintext: bytes, key: bytes, iv: bytes) -> bytes:
    if len(iv) != BLOCK_SIZE:
        raise ValueError("AES-CBC IV must be 16 bytes")
    previous = iv
    ciphertext = bytearray()
    for offset in range(0, len(pkcs7_pad(plaintext)), BLOCK_SIZE):
        block = pkcs7_pad(plaintext)[offset : offset + BLOCK_SIZE]
        encrypted = aes128_encrypt_block(_xor(block, previous), key)
        ciphertext.extend(encrypted)
        previous = encrypted
    return bytes(ciphertext)


def aes128_cbc_decrypt(ciphertext: bytes, key: bytes, iv: bytes) -> bytes:
    if len(iv) != BLOCK_SIZE:
        raise ValueError("AES-CBC IV must be 16 bytes")
    if not ciphertext or len(ciphertext) % BLOCK_SIZE:
        raise ValueError("ciphertext length must be a positive multiple of 16")
    previous = iv
    plaintext = bytearray()
    for offset in range(0, len(ciphertext), BLOCK_SIZE):
        block = ciphertext[offset : offset + BLOCK_SIZE]
        decrypted = aes128_decrypt_block(block, key)
        plaintext.extend(_xor(decrypted, previous))
        previous = block
    return pkcs7_unpad(bytes(plaintext))
